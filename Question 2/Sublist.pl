% prefix/2 checks if the first argument is prefix of the second argument.

% An empty list is a prefix of every list.
prefix([], Y).

% 
prefix([H|TX], [H|TY]) :- 
		prefix(TX, TY). 
		
% ####################################################################################################################			

reverse([], []).
reverse([H|T], L2) :- 
		reverse(T, L3),
		append(L3, [H], L2).
		
% ####################################################################################################################			

suffix(X, Y) :- 
		reverse(Y, Y_rev), 
		reverse(X, X_rev), 
		prefix(X_rev, Y_rev).

% ####################################################################################################################			

sublist(X, Y) :- 
		suffix(Suffix, Y),
		prefix(X, Suffix).

